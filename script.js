const languageButton = document.querySelector(".lang-toggle");
const languageLabels = document.querySelectorAll("[data-lang-label]");
const translatableNodes = document.querySelectorAll("[data-i18n]");

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.es;
  document.documentElement.lang = lang;
  document.body.dataset.lang = lang;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  languageLabels.forEach((label) => {
    label.classList.toggle("active", label.dataset.langLabel === lang);
  });

  localStorage.setItem("portfolioLanguage", lang);
}

const savedLanguage = localStorage.getItem("portfolioLanguage");
setLanguage(savedLanguage === "en" ? "en" : "es");

languageButton.addEventListener("click", () => {
  const nextLanguage = document.body.dataset.lang === "en" ? "es" : "en";
  setLanguage(nextLanguage);
});

const canvas = document.getElementById("heroCanvas");
const ctx = canvas.getContext("2d");
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");
let width = 0;
let height = 0;
let particles = [];

function resizeCanvas() {
  const ratio = window.devicePixelRatio || 1;
  width = canvas.offsetWidth;
  height = canvas.offsetHeight;
  canvas.width = width * ratio;
  canvas.height = height * ratio;
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
  buildParticles();
}

function buildParticles() {
  particles = Array.from({ length: 42 }, (_, index) => ({
    progress: (index / 42) * 1.2,
    speed: 0.0015 + (index % 7) * 0.00018,
    size: 2 + (index % 4),
    lane: index % 4
  }));
}

function pointAt(progress, lane) {
  const startX = width * 0.54;
  const endX = width * 0.92;
  const centerY = height * (0.26 + lane * 0.13);
  const curve = Math.sin(progress * Math.PI) * height * (0.16 - lane * 0.018);

  return {
    x: startX + (endX - startX) * progress,
    y: centerY + curve
  };
}

function drawFunnel() {
  ctx.clearRect(0, 0, width, height);

  const stages = [
    { label: "ICP", x: width * 0.55, y: height * 0.28, r: 52, color: "rgba(134, 187, 216, 0.55)" },
    { label: "MSG", x: width * 0.68, y: height * 0.42, r: 64, color: "rgba(10, 216, 129, 0.5)" },
    { label: "CRM", x: width * 0.8, y: height * 0.57, r: 58, color: "rgba(245, 184, 75, 0.44)" },
    { label: "PIPE", x: width * 0.9, y: height * 0.72, r: 70, color: "rgba(14, 121, 81, 0.62)" }
  ];

  ctx.lineWidth = 1.5;
  stages.forEach((stage, index) => {
    if (index < stages.length - 1) {
      const next = stages[index + 1];
      ctx.beginPath();
      ctx.moveTo(stage.x, stage.y);
      ctx.lineTo(next.x, next.y);
      ctx.strokeStyle = "rgba(255, 255, 255, 0.22)";
      ctx.stroke();
    }
  });

  stages.forEach((stage) => {
    const gradient = ctx.createRadialGradient(stage.x, stage.y, 4, stage.x, stage.y, stage.r);
    gradient.addColorStop(0, stage.color);
    gradient.addColorStop(1, "rgba(255, 255, 255, 0)");

    ctx.beginPath();
    ctx.arc(stage.x, stage.y, stage.r, 0, Math.PI * 2);
    ctx.fillStyle = gradient;
    ctx.fill();

    ctx.beginPath();
    ctx.arc(stage.x, stage.y, 13, 0, Math.PI * 2);
    ctx.fillStyle = "rgba(255, 255, 255, 0.9)";
    ctx.fill();
  });

  particles.forEach((particle) => {
    particle.progress += particle.speed;
    if (particle.progress > 1) particle.progress = 0;

    const point = pointAt(particle.progress, particle.lane);
    ctx.beginPath();
    ctx.arc(point.x, point.y, particle.size, 0, Math.PI * 2);
    ctx.fillStyle = particle.lane === 2 ? "rgba(245, 184, 75, 0.9)" : "rgba(10, 216, 129, 0.85)";
    ctx.fill();
  });

  if (!prefersReducedMotion.matches) {
    requestAnimationFrame(drawFunnel);
  }
}

window.addEventListener("resize", resizeCanvas);
resizeCanvas();
drawFunnel();
